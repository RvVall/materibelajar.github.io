# ELIF = else if statement

nama = input("Nama kamu siapa? ")

if nama == "agus": # kondisi 1
	print("Hai ganteeeeng !") # aksi true 1
elif nama == "budi": # kondisi 2
	print("Hai si kece bangeeeet!!") # aksi true 2
elif nama == "mario": # kondisi 3
	print("Hai humooooreeeesh!") # aksi true 3
else:
	print("gak kenal siapa yhhh!!!") # aksi false

print("\nini adalah akhir dari program\n")